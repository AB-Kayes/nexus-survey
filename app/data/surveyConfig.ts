export function getBranch(occupation: string): OccupationBranch {
  if (occupation === "Agency Professional") return "agency";
  if (occupation === "E-commerce Seller") return "ecommerce";
  if (occupation === "Marketing / Sales Professional") return "marketing_sales";
  if (occupation === "Business Owner / Founder") return "business_owner";
  return "generic";
}

